<?php
/**
 * includes/course_engine.php
 *
 * 7-day speech improvement course — engine.
 *
 * Lifecycle:
 *   1. Parent finishes an eval → sees the 5-axis report → clicks Buy ₹99/₹199/₹350
 *   2. course-start.php charges wallet, calls course_create()
 *      - new course row with anchor_sentence + weak_axes JSON + sku
 *   3. Parent visits /course.php?id=N → today's task generated + cached
 *      - same task all day; reusing day records latest recording
 *   4. Child does the task with parent (2/5/10 min based on SKU)
 *   5. Child records the ANCHOR SENTENCE (same all 7 days)
 *   6. course-day.php scores the anchor recording (5-axis snapshot)
 *      and advances last_active_at + completed_at on day row
 *   7. Tomorrow: parent comes back → next day's task auto-generated
 *
 * Missed-days rule (strict): if today is 2+ days after last completion,
 * course is marked failed. 1 day grace = OK. No refunds.
 *
 * Day 8: course status = 'completed'. /course.php shows final progress chart
 * + delta vs original eval + Week 2 upsell.
 *
 * Reuses: eval_clinical.php's _clinical_compute_priors() for axis scoring,
 * wallet helpers, claude.php's claude_json() for task generation.
 */

require_once __DIR__ . '/claude.php';
require_once __DIR__ . '/eval_clinical.php';  // for _clinical_compute_priors


// ─────────────────────────────────────────────────────────────
// Schema migration (idempotent)
// ─────────────────────────────────────────────────────────────
function _course_ensure_schema(): void {
    static $done = false;
    if ($done) return;

    db()->exec("CREATE TABLE IF NOT EXISTS eval_courses (
        id                INTEGER PRIMARY KEY AUTOINCREMENT,
        parent_id         INTEGER NOT NULL,
        child_id          INTEGER NOT NULL REFERENCES children(id) ON DELETE CASCADE,
        eval_session_id   INTEGER REFERENCES eval_sessions(id) ON DELETE SET NULL,
        sku               TEXT NOT NULL,         -- 'course_speech_2min' | '5min' | '10min'
        daily_minutes     INTEGER NOT NULL,      -- 2 | 5 | 10
        price_paid        INTEGER NOT NULL,      -- 99 | 199 | 350
        weak_axes_json    TEXT,                  -- JSON: top 2 weakest axes from eval, with baseline scores
        anchor_sentence   TEXT NOT NULL,         -- same sentence all 7 days
        anchor_lang       TEXT NOT NULL DEFAULT 'hi',   -- 'hi' or 'en'
        status            TEXT NOT NULL DEFAULT 'active',  -- active | completed | failed | abandoned
        started_at        TEXT DEFAULT CURRENT_TIMESTAMP,
        completed_at      TEXT,
        last_active_at    TEXT,
        notes             TEXT
    );");
    db()->exec("CREATE INDEX IF NOT EXISTS idx_courses_parent ON eval_courses(parent_id, status);");
    db()->exec("CREATE INDEX IF NOT EXISTS idx_courses_child ON eval_courses(child_id);");

    db()->exec("CREATE TABLE IF NOT EXISTS eval_course_days (
        id                INTEGER PRIMARY KEY AUTOINCREMENT,
        course_id         INTEGER NOT NULL REFERENCES eval_courses(id) ON DELETE CASCADE,
        day_no            INTEGER NOT NULL,         -- 1..7
        task_md           TEXT,                     -- parent-facing task instructions (generated by Claude)
        task_target_axis  TEXT,                     -- which axis this day targets (articulation/fluency/etc)
        task_generated_at TEXT,
        recording_path    TEXT,                     -- audio of child reading anchor sentence
        transcript        TEXT,                     -- Web Speech transcript of the recording
        acoustic_json     TEXT,                     -- WPM, confidence, etc — same shape as eval_questions
        axis_snapshot_json TEXT,                    -- 5-axis scores for THIS day's anchor reading
        parent_note       TEXT,
        completed_at      TEXT,
        UNIQUE(course_id, day_no)
    );");
    db()->exec("CREATE INDEX IF NOT EXISTS idx_course_days ON eval_course_days(course_id, day_no);");

    // Seed the 3 SKU prices (idempotent INSERT OR REPLACE)
    try {
        $skus = [
            ['course_speech_2min',  '7-Day Speech Course — 2 min/day',  99,  'parent', 1],
            ['course_speech_5min',  '7-Day Speech Course — 5 min/day',  199, 'parent', 1],
            ['course_speech_10min', '7-Day Speech Course — 10 min/day', 350, 'parent', 1],
        ];
        foreach ($skus as $s) {
            db()->prepare("INSERT OR REPLACE INTO service_prices
                           (service_key, label, price, audience, is_active)
                           VALUES (?, ?, ?, ?, ?)")
               ->execute($s);
        }
    } catch (Throwable $e) {
        error_log('[course schema seed prices] ' . $e->getMessage());
    }

    $done = true;
}


// ─────────────────────────────────────────────────────────────
// Anchor sentence picker — hardcoded library
// ─────────────────────────────────────────────────────────────
/**
 * Returns ['sentence' => string, 'lang' => 'hi'|'en', 'target_axis' => string]
 *
 * The anchor sentence is picked based on the weakest axis in the clinical
 * report. Each sentence is engineered to PROBE the targeted weakness, so
 * day-to-day variation in the recording reveals real progress.
 *
 * Picked once at course creation, used for all 7 days (clean comparison).
 */
function course_pick_anchor_sentence(array $clinical_axes, bool $is_hindi): array {
    // Find the lowest-scoring axis (excluding null/N-A like narrative when not assessed)
    $weakest_axis = 'fluency';
    $weakest_score = 101;
    foreach (['articulation','fluency','vocabulary','grammar','narrative'] as $k) {
        $score = $clinical_axes[$k]['score'] ?? null;
        if ($score === null) continue;
        if ((int)$score < $weakest_score) {
            $weakest_score = (int)$score;
            $weakest_axis = $k;
        }
    }

    // Sentence library — one Hindi + English version per axis target
    $lib = [
        'articulation' => [
            // Heavy on consonant blends, retroflexes (ट, ठ, ड), aspirated sounds
            'hi' => 'मेरे प्यारे दादा-दादी कल गाँव से ट्रेन में आए और सब बच्चों के लिए मिठाई लाए।',
            'en' => 'My grandparents brought sweet little chocolates and cricket bats from the big train station.',
        ],
        'fluency' => [
            // Long enough to require smooth flow, simple words so flow itself is the test
            'hi' => 'आज सुबह मैं उठा, ब्रश किया, नाश्ता खाया और फिर अपने दोस्तों के साथ खेलने बाहर गया।',
            'en' => 'This morning I woke up, brushed my teeth, ate breakfast, and then went outside to play with my friends.',
        ],
        'vocabulary' => [
            // Variety of word types — nouns, verbs, adjectives, time markers
            'hi' => 'कल शाम को मेरी छोटी बहन ने अपनी नई नीली साइकिल पर खूब तेज़ चलाई।',
            'en' => 'Yesterday evening my little sister rode her brand new blue bicycle very fast around the park.',
        ],
        'grammar' => [
            // Multiple tenses, postpositions, sentence connectors
            'hi' => 'जब बारिश हो रही थी, मेरी माँ ने मुझे छाता दिया और मैंने स्कूल जाते समय खूब मज़े किए।',
            'en' => 'When it was raining, my mother gave me an umbrella and I had so much fun on my way to school.',
        ],
        'narrative' => [
            // Sequencing + cause/effect + 3+ events
            'hi' => 'पहले मैंने अपना होमवर्क पूरा किया, फिर खाना खाया, और अंत में दादी के साथ कहानी सुनी।',
            'en' => 'First I finished all of my homework, then I ate my dinner, and finally I listened to a story with grandma.',
        ],
    ];

    $entry = $lib[$weakest_axis] ?? $lib['fluency'];
    $sentence = $is_hindi ? $entry['hi'] : $entry['en'];

    return [
        'sentence'    => $sentence,
        'lang'        => $is_hindi ? 'hi' : 'en',
        'target_axis' => $weakest_axis,
    ];
}


