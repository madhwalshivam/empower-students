<?php
/**
 * includes/parent_eval_v3.php
 *
 * ₹1000 Parent Evaluation v3 — the "listening" interview.
 *
 * Replaces the narrative-deep, solution-mode reflection with a
 * breadth-first listening interview that:
 *   1. Maps the parent's whole landscape across 9 life areas in 13-15 turns
 *   2. Never gives advice — only listens, mirrors, and surfaces what is heavy
 *   3. Generates a STRUCTURED LISTING report:
 *        • One row per area (max 9)
 *        • Index 0-100 (burden — high = heavy)
 *        • Severity: low/moderate/high/critical
 *        • Urgency: can_wait/this_month/this_week/today
 *        • Plan for course (which day addresses it)
 *   4. Narrative is still generated by pr_finalise() — but lands in the
 *      PDF as a secondary "your story" section, not the primary report.
 *
 * Provided functions:
 *   pr_v3_areas(): array
 *       The 9 area definitions (key, label_en, label_hi, sample_questions).
 *
 *   pr_v3_build_guide_system($current_phase, $turn_no, $target, $cap, $children_context, $language): string
 *       Replacement system prompt for pr_decide_next() — drops advice mode,
 *       adds area coverage tracking.
 *
 *   pr_v3_coverage_snapshot(int $session_id): array
 *       Returns ['areas_covered' => [...], 'areas_pending' => [...]].
 *
 *   pr_v3_generate_listing(int $session_id, bool $force = false): bool
 *       Generates the structured 9-area listing JSON and stores in
 *       parent_reflect_sessions.v3_listing_json. Idempotent.
 *
 *   pr_v3_render_listing_html(?string $listing_json): string
 *       Render-ready HTML for the immediate-result screen and PDF.
 */

