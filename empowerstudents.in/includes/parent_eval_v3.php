// ───────────────────────────────────────────────────────────
// Schema migration (idempotent)
// ───────────────────────────────────────────────────────────
function _pr_v3_ensure_columns(): void {
    static $done = false;
    if ($done) return;
    $done = true;
    try {
        $cols = db()->query("PRAGMA table_info(parent_reflect_sessions)")->fetchAll();
        $names = array_column($cols, 'name');
        foreach ([
            'v3_listing_json'      => 'TEXT',
            'v3_listing_at'        => 'TEXT',
            'v3_paused'            => "INTEGER DEFAULT 0",
            'v3_paused_at'         => 'TEXT',
            'engine_version'       => "TEXT DEFAULT 'v3'",
            'eval_areas_covered'   => 'TEXT',   // CSV of area keys already touched
        ] as $col => $type) {
            if (!in_array($col, $names, true)) {
                db()->exec("ALTER TABLE parent_reflect_sessions ADD COLUMN $col $type");
            }
        }
    } catch (Throwable $e) {
        error_log('[pr_v3 schema] ' . $e->getMessage());
    }
}


