// ───────────────────────────────────────────────────────────
// The new listening prompt — REPLACES pr_build_guide_system
// ───────────────────────────────────────────────────────────
function pr_v3_build_guide_system(int $current_phase, int $turn_no, int $target, int $cap, string $children_context, string $language = 'hi', array $coverage = []): string {
    $areas = pr_v3_areas();
    $areas_covered = $coverage['areas_covered'] ?? [];
    $areas_pending = $coverage['areas_pending'] ?? array_keys($areas);

    $covered_list = empty($areas_covered) ? '(none yet)' : implode(', ', $areas_covered);
    $pending_list = empty($areas_pending) ? '(all covered)' : implode(', ', $areas_pending);

    $area_descriptions = '';
    foreach ($areas as $key => $a) {
        $area_descriptions .= "  • {$key} ({$a['label_en']}) — sample: \"{$a['sample_q_en']}\"\n";
    }

    $turns_left = max(0, $target - $turn_no);

    $lang_block = ($language === 'hi')
        ? "LANGUAGE: Speak in conversational HINDI (Devanagari). Warm, natural, code-switching with English words for modern concepts (anxiety, depression, screen, therapy, peers) is fine — natural for Indian parents. Use 'आप' (respectful). NEVER literary Hindi."
        : "LANGUAGE: Speak in warm, conversational English.";

    return <<<SYS
You are conducting a private, voice-driven LISTENING interview with a parent. Your job is to map the WHOLE LANDSCAPE of their life across 9 areas, in roughly 13-15 turns total. You are a wise, calm, completely non-judgemental listener.

CRITICAL RULE — YOU DO NOT GIVE ADVICE.
This interview is for HEARING, not fixing. Even if the parent says something where the "answer" feels obvious to you, your job is to:
  - acknowledge what you heard
  - reflect a phrase or feeling back
  - ASK ABOUT THE NEXT AREA
You NEVER say "you might try…", "have you considered…", "one thing that helps…", "what often works is…". No solutions, no experiments, no homework. The 7-day course (which they may buy later) is where solutions happen. Your job is the listing of the problem — and listing the problem IS the start of the solution.

YOUR JOB STRUCTURE:
- Cover 9 areas with one question each (plus follow-ups when needed). Total 13-15 turns.
- The parent should leave feeling fully heard across all dimensions of their life — not deeply analysed on one.

THE 9 AREAS YOU MUST COVER:
{$area_descriptions}

COVERAGE STATE:
- Already covered organically (don't re-ask): {$covered_list}
- Still to ask: {$pending_list}

PARENT'S CHILDREN (context — DO NOT name a specific child unless the parent does first):
{$children_context}

{$lang_block}

CORE PRINCIPLES:
- One question per turn, under 30 words.
- ALWAYS reference / acknowledge / mirror something from the parent's previous answer before asking the next area's question. Examples:
   • "When you say your father-in-law disagrees with everything — I hear how alone you feel in those decisions. Coming to a different area — how is your child doing with friends?"
   • "जब आप कहते हैं 'थक गई हूँ' — सुना मैंने। अब एक और चीज़ पूछूँ — पैसे और काम का कैसा pressure है इन दिनों?"
- Honour silence. If they pause or struggle, do NOT fill it with another question — gently rephrase or say "जो भी आये, कोई जल्दी नहीं" / "Take your time, no rush".
- Be Indian-context aware: joint families, log kya kahenge, izzat, financial reality of therapy, generational beliefs.
- If the parent surfaces something heavy in passing (e.g. "I sometimes cry alone"), spend ONE follow-up there to honour it, then GENTLY move to the next area. Do NOT dwell.

ADAPTIVE COVERAGE:
- If the parent already touched an area organically in a previous turn, DO NOT re-ask. Mark it covered and move to a pending area.
- If the parent has no joint family / has lost a parent / is a single parent — skip the relevant area entirely. Add an empty note for it later.
- If the parent's answers are very short (5-15 words each), they're nervous or guarded — ask shorter, easier questions; do not push depth.
- If the parent gives long answers (60+ words), they need the space — give one follow-up there, then keep moving across areas.

SAFETY OVERRIDE (NON-NEGOTIABLE):
If the parent describes self-harm thoughts, harm to the child, active domestic violence, or imminent crisis:
  - Set safety_red_flag: 1
  - Set intent: "slow"
  - Set done: false (one more turn at least)
  - In next_question: gently surface what you heard, validate it makes sense to feel that way, softly mention iCall (9152987821) or Vandrevala Foundation (1860-2662-345). Do NOT moralise. Do NOT say "you should call".

SIGNALS (track every turn, 0.0–1.0):
- child_distress, marital_stress, in_law_stress, financial_stress, parent_burnout, isolation, safety_red_flag (0|1)

OUTPUT — return ONLY this JSON, no prose:
{
  "reflection": "1-2 sentences mirroring what they said, in their words. Empty string for very first turn.",
  "tone_insight": "1 short sentence on what their voice + words suggest. Empty if not clear.",
  "covered_area": "key of the area their LAST answer mostly covered — one of: child_daily, child_study, child_peers, child_emotion, future_hopes, couple, family, money_work, self. Or empty string if their answer covered nothing structured.",
  "next_area": "key of the next area you will ask about. Pick from pending areas above.",
  "intent": "probe | mirror | move_on | slow | close",
  "signals": {
    "child_distress": 0.0,
    "marital_stress": 0.0,
    "in_law_stress": 0.0,
    "financial_stress": 0.0,
    "parent_burnout": 0.0,
    "isolation": 0.0,
    "safety_red_flag": 0
  },
  "next_question": "The single next question, under 30 words. MUST briefly mirror their last answer before asking about the next_area. NO advice, NO solutions, NO 'you might try'.",
  "done": false
}

PACING:
- This is turn {$turn_no}. Target is {$target} turns (hard cap {$cap}). {$turns_left} turns left.
- If turn_no >= ({$target} - 1) AND most areas covered (5+), START WINDING UP. Set done: true.
- Closing question (when done=true): a warm "is there anything else weighing on you that we didn't touch today?" — give them one last open invitation, NOT a summary. No advice.

REMEMBER: You are a listener, not a counsellor. The structured listing report (separately generated) is what will help the parent see all of this clearly. Your only job is to make them feel HEARD across all areas.
SYS;
}


// ───────────────────────────────────────────────────────────
// Coverage tracking — read from session, mark areas as covered
// ───────────────────────────────────────────────────────────
function pr_v3_coverage_snapshot(int $session_id): array {
    _pr_v3_ensure_columns();
    $all_keys = array_keys(pr_v3_areas());

    // Coverage is tracked in parent_reflect_sessions.eval_areas_covered (CSV of keys)
    // PLUS — recovered from parent_reflect_turns.signals_json[covered_area] for resilience
    $st = db()->prepare("SELECT eval_areas_covered FROM parent_reflect_sessions WHERE id = ?");
    $st->execute([$session_id]);
    $csv = (string)($st->fetchColumn() ?: '');
    $stored = array_filter(array_map('trim', explode(',', $csv)));

    // Also walk turns to pick up any 'covered_area' fields stored
    $tst = db()->prepare("SELECT signals_json FROM parent_reflect_turns WHERE session_id = ?");
    $tst->execute([$session_id]);
    $from_turns = [];
    while ($row = $tst->fetch()) {
        $sj = json_decode($row['signals_json'] ?: '{}', true);
        if (is_array($sj) && !empty($sj['covered_area']) && in_array($sj['covered_area'], $all_keys, true)) {
            $from_turns[] = $sj['covered_area'];
        }
    }

    $covered = array_values(array_unique(array_merge($stored, $from_turns)));
    $covered = array_values(array_intersect($covered, $all_keys));
    $pending = array_values(array_diff($all_keys, $covered));

    return [
        'areas_covered' => $covered,
        'areas_pending' => $pending,
        'pct_covered'   => count($all_keys) > 0 ? (int)round(count($covered) / count($all_keys) * 100) : 0,
    ];
}


function pr_v3_mark_area_covered(int $session_id, string $area_key): void {
    _pr_v3_ensure_columns();
    $valid = array_keys(pr_v3_areas());
    if (!in_array($area_key, $valid, true)) return;
    $cs = pr_v3_coverage_snapshot($session_id);
    if (in_array($area_key, $cs['areas_covered'], true)) return;
    $cs['areas_covered'][] = $area_key;
    $new_csv = implode(',', array_unique($cs['areas_covered']));
    db()->prepare("UPDATE parent_reflect_sessions SET eval_areas_covered = ? WHERE id = ?")
       ->execute([$new_csv, $session_id]);
}


// ───────────────────────────────────────────────────────────
// Generate the STRUCTURED LISTING from a completed session
// ───────────────────────────────────────────────────────────
function pr_v3_generate_listing(int $session_id, bool $force = false): bool {
    _pr_v3_ensure_columns();

    $st = db()->prepare("SELECT * FROM parent_reflect_sessions WHERE id = ?");
    $st->execute([$session_id]);
    $session = $st->fetch();
    if (!$session || $session['status'] !== 'completed') return false;
    if (!$force && !empty($session['v3_listing_json'])) return true;

    // Pull turns for context
    $tst = db()->prepare("SELECT question, transcript, signals_json FROM parent_reflect_turns
                          WHERE session_id = ? AND transcript IS NOT NULL AND transcript != ''
                          ORDER BY turn_no ASC");
    $tst->execute([$session_id]);
    $turns = $tst->fetchAll();
    if (empty($turns)) return false;

    $convo_lines = [];
    foreach ($turns as $t) {
        $q = trim((string)$t['question']);
        $a = trim((string)$t['transcript']);
        if ($q !== '') $convo_lines[] = "AI: " . mb_substr($q, 0, 250);
        if ($a !== '') $convo_lines[] = "PARENT: " . mb_substr($a, 0, 500);
    }
    $convo = implode("\n", $convo_lines);
    if (mb_strlen($convo) > 9000) $convo = mb_substr($convo, 0, 9000) . "\n[truncated]";

    // Detect language
    $pst = db()->prepare("SELECT mother_tongue FROM children WHERE parent_id = ?");
    $pst->execute([(int)$session['parent_id']]);
    $hi = 0; $en = 0;
    while ($r = $pst->fetch()) {
        $mt = strtolower(trim((string)$r['mother_tongue']));
        if (strpos($mt, 'hindi') !== false || $mt === 'hi') $hi++; else $en++;
    }
    $language = ($hi >= $en) ? 'hi' : 'en';

    $areas = pr_v3_areas();
    $area_keys_list = implode(', ', array_keys($areas));
    $area_specs = '';
    foreach ($areas as $key => $a) {
        $area_specs .= "  - {$key}: \"{$a['label_en']}\" — { use $key as the JSON object key }\n";
    }

    $lang_block = $language === 'hi'
        ? "Write 'finding', 'severity_note', and 'one_line_summary' fields in conversational HINDI (Devanagari, respectful 'आप'). Code-switching with English words is fine. Use phrases the parent ACTUALLY USED — quote 1-2 short phrases per area where natural."
        : "Write 'finding', 'severity_note', and 'one_line_summary' fields in warm conversational English. Use 1-2 short quoted phrases from the parent's actual answers where natural.";

    $sys = <<<SYS
You are a senior clinical psychologist generating a STRUCTURED LISTING REPORT from a parent's listening interview. The interview mapped 9 life areas. You will now produce one report row per area, plus an overall summary.

{$lang_block}

CORE RULES:
1. This is a LISTING — NOT a solutions report. Do NOT write "try this" or "you should". The listing names the problem; the 7-day course addresses it.
2. For areas NOT mentioned in the conversation, mark "covered": false and use neutral language ("This area was not explored in today's interview.") — DO NOT make up findings.
3. INDEX (0-100) = burden score. 0 = no burden detected. 100 = severe, immediate. Calibrate honestly:
   - 0-20: green (no significant concern)
   - 21-40: light amber (minor concern, monitor)
   - 41-60: amber (real concern, attention needed)
   - 61-80: orange (heavy concern, urgent attention)
   - 81-100: red (critical, address immediately)
4. SEVERITY: low | moderate | high | critical (matches the bands above)
5. URGENCY: can_wait | this_month | this_week | today (today only if safety_red_flag or acute crisis)
6. COURSE_DAY: which of the 7 days of the home course best addresses this area. Use:
     1 = praise practice (child_climate)
     2 = couple connection (couple)
     3 = discipline reset (child behaviour)
     4 = self-care window (parent wellbeing)
     5 = boundary practice (joint family)
     6 = reach out (support / isolation)
     7 = reflection share (couple harmony)
   Pick the day whose theme most directly addresses this area. For areas that have no good match, set course_day to null.

OUTPUT — JSON ONLY, this exact shape:
{
  "areas": {
    "child_daily":    { "covered": true|false, "finding": "...", "index": 0-100, "severity": "...", "urgency": "...", "severity_note": "...", "course_day": 1-7 or null },
    "child_study":    { ... },
    "child_peers":    { ... },
    "child_emotion":  { ... },
    "future_hopes":   { ... },
    "couple":         { ... },
    "family":         { ... },
    "money_work":     { ... },
    "self":           { ... }
  },
  "overall_index": 0-100,
  "top_3_urgent_areas": ["area_key", "area_key", "area_key"],
  "one_line_summary": "ONE warm sentence that names what we heard today (in {$language}).",
  "course_plan": [
    { "day": 1, "theme": "Praise practice", "addresses_areas": ["child_daily", "child_emotion"], "why": "1 line tailored to THIS parent's situation" },
    { "day": 2, "theme": "Couple connection", "addresses_areas": ["couple"], "why": "..." },
    { "day": 3, "theme": "Discipline reset", "addresses_areas": ["child_emotion"], "why": "..." },
    { "day": 4, "theme": "Self-care window", "addresses_areas": ["self"], "why": "..." },
    { "day": 5, "theme": "Boundary practice", "addresses_areas": ["family"], "why": "..." },
    { "day": 6, "theme": "Reach out", "addresses_areas": ["self"], "why": "..." },
    { "day": 7, "theme": "Reflection share", "addresses_areas": ["couple"], "why": "..." }
  ],
  "safety_flag": 0|1
}

Area keys must be: {$area_keys_list}
SYS;

    $usr = "=== Conversation transcript ===\n$convo\n\nGenerate the structured listing JSON now. JSON only.";

    $resp = function_exists('claude_chat')
        ? claude_chat($sys, [['role' => 'user', 'content' => $usr]], 3500, 0.4)
        : '';
    $clean = trim((string)$resp);
    if ($clean === '') {
        error_log("[pr_v3] empty Claude response for session $session_id");
        return false;
    }
    if (strpos($clean, '```') !== false) {
        $clean = preg_replace('/^```(?:json)?\s*/i', '', $clean);
        $clean = preg_replace('/\s*```\s*$/', '', $clean);
        $clean = trim($clean);
    }
    $parsed = json_decode($clean, true);
    if (!is_array($parsed) || empty($parsed['areas'])) {
        error_log("[pr_v3] could not parse listing for session $session_id");
        return false;
    }

    // Add metadata
    $parsed['language'] = $language;
    $parsed['generated_at'] = gmdate('Y-m-d H:i:s');
    $parsed['session_id'] = $session_id;

    db()->prepare("UPDATE parent_reflect_sessions
                    SET v3_listing_json = ?, v3_listing_at = CURRENT_TIMESTAMP
                    WHERE id = ?")
       ->execute([json_encode($parsed, JSON_UNESCAPED_UNICODE), $session_id]);

    return true;
}


// ───────────────────────────────────────────────────────────
// Render the structured listing as HTML (for screen + PDF)
// ───────────────────────────────────────────────────────────
function pr_v3_render_listing_html(?string $listing_json): string {
    if (empty($listing_json)) return '';
    $data = json_decode($listing_json, true);
    if (!is_array($data) || empty($data['areas'])) return '';
    $lang = $data['language'] ?? 'en';
    $is_hindi = $lang === 'hi';
    $areas = pr_v3_areas();

    $severity_color = [
        'critical' => ['bg' => 'bg-rose-100',    'text' => 'text-rose-800',    'border' => 'border-rose-300',    'bar' => 'bg-rose-500'],
        'high'     => ['bg' => 'bg-orange-100',  'text' => 'text-orange-800',  'border' => 'border-orange-300',  'bar' => 'bg-orange-500'],
        'moderate' => ['bg' => 'bg-amber-100',   'text' => 'text-amber-800',   'border' => 'border-amber-300',   'bar' => 'bg-amber-500'],
        'low'      => ['bg' => 'bg-emerald-100', 'text' => 'text-emerald-800', 'border' => 'border-emerald-300', 'bar' => 'bg-emerald-500'],
    ];
    $urgency_label = $is_hindi
        ? ['today' => 'आज ही', 'this_week' => 'इस हफ़्ते', 'this_month' => 'इस महीने', 'can_wait' => 'अभी रुक सकता है']
        : ['today' => 'TODAY', 'this_week' => 'This week', 'this_month' => 'This month', 'can_wait' => 'Can wait'];
    $severity_label = $is_hindi
        ? ['critical' => 'गंभीर', 'high' => 'भारी', 'moderate' => 'मध्यम', 'low' => 'हल्का']
        : ['critical' => 'Critical', 'high' => 'High', 'moderate' => 'Moderate', 'low' => 'Low'];

    $html  = '<div class="space-y-3">';

    // One-line summary
    if (!empty($data['one_line_summary'])) {
        $html .= '<div class="bg-emerald-50 border border-emerald-200 rounded-xl p-4 mb-3">';
        $html .= '<div class="text-xs uppercase tracking-wider font-bold text-emerald-700 mb-1">';
        $html .= ($is_hindi ? '🌿 आज हमने जो सुना' : '🌿 What we heard today');
        $html .= '</div>';
        $html .= '<p class="text-base text-slate-900 leading-relaxed">' . htmlspecialchars($data['one_line_summary']) . '</p>';
        $html .= '</div>';
    }

    // Per-area rows
    foreach ($areas as $key => $meta) {
        $a = $data['areas'][$key] ?? null;
        $label = $is_hindi ? $meta['label_hi'] : $meta['label_en'];
        $emoji = $meta['emoji'];

        if (!$a || empty($a['covered'])) {
            $html .= '<div class="border border-slate-200 rounded-xl p-3 opacity-60">';
            $html .= '<div class="flex items-center justify-between">';
            $html .= '<div class="flex items-center gap-2"><span class="text-lg">' . $emoji . '</span><span class="font-semibold text-slate-600">' . htmlspecialchars($label) . '</span></div>';
            $html .= '<span class="text-xs text-slate-500 italic">' . ($is_hindi ? 'आज नहीं पूछा गया' : 'Not explored today') . '</span>';
            $html .= '</div></div>';
            continue;
        }

        $severity = $a['severity'] ?? 'moderate';
        $color = $severity_color[$severity] ?? $severity_color['moderate'];
        $index = (int)($a['index'] ?? 0);
        $urg = $a['urgency'] ?? 'this_month';

        $html .= '<div class="border ' . $color['border'] . ' rounded-xl p-3">';
        $html .= '<div class="flex items-center justify-between mb-2 gap-2">';
        $html .= '<div class="flex items-center gap-2 min-w-0">';
        $html .= '<span class="text-xl flex-shrink-0">' . $emoji . '</span>';
        $html .= '<span class="font-bold text-slate-900 truncate">' . htmlspecialchars($label) . '</span>';
        $html .= '</div>';
        $html .= '<div class="flex items-center gap-2 flex-shrink-0">';
        $html .= '<span class="text-xs px-2 py-0.5 rounded-full font-semibold ' . $color['bg'] . ' ' . $color['text'] . '">' . htmlspecialchars($severity_label[$severity] ?? $severity) . '</span>';
        $html .= '<span class="text-sm font-bold text-slate-900">' . $index . '</span>';
        $html .= '</div></div>';

        // Burden bar
        $html .= '<div class="w-full bg-slate-100 rounded-full h-1.5 mb-2 overflow-hidden">';
        $html .= '<div class="' . $color['bar'] . ' h-1.5 rounded-full" style="width:' . $index . '%"></div>';
        $html .= '</div>';

        // Finding text
        $finding = trim((string)($a['finding'] ?? ''));
        if ($finding !== '') {
            $html .= '<p class="text-sm text-slate-700 leading-relaxed mb-2">' . htmlspecialchars($finding) . '</p>';
        }

        // Urgency + course day
        $html .= '<div class="flex items-center gap-2 text-xs">';
        $html .= '<span class="px-2 py-0.5 bg-slate-100 rounded text-slate-700 font-medium">⏱ ' . htmlspecialchars($urgency_label[$urg] ?? $urg) . '</span>';
        if (!empty($a['course_day'])) {
            $cd = (int)$a['course_day'];
            $html .= '<span class="px-2 py-0.5 bg-indigo-100 text-indigo-800 rounded font-medium">📅 ' . ($is_hindi ? 'Course Day ' : 'Course Day ') . $cd . '</span>';
        }
        $html .= '</div>';

        $html .= '</div>';
    }

    // Overall index
    if (isset($data['overall_index'])) {
        $oi = (int)$data['overall_index'];
        $oi_color = $oi >= 60 ? 'rose' : ($oi >= 40 ? 'amber' : 'emerald');
        $html .= '<div class="bg-' . $oi_color . '-50 border-2 border-' . $oi_color . '-200 rounded-xl p-4 mt-4">';
        $html .= '<div class="flex items-center justify-between">';
        $html .= '<span class="font-bold text-slate-900">' . ($is_hindi ? '🎯 कुल burden index' : '🎯 Overall burden index') . '</span>';
        $html .= '<span class="text-3xl font-bold text-' . $oi_color . '-700">' . $oi . '<span class="text-sm">/100</span></span>';
        $html .= '</div>';
        $html .= '</div>';
    }

    // Course plan
    if (!empty($data['course_plan'])) {
        $html .= '<div class="bg-indigo-50 border border-indigo-200 rounded-xl p-4 mt-4">';
        $html .= '<h3 class="font-bold text-indigo-900 mb-2">' . ($is_hindi ? '📅 7-दिन के Course का plan — आपके लिए' : '📅 7-Day Course plan — for you') . '</h3>';
        $html .= '<div class="space-y-1.5">';
        foreach ($data['course_plan'] as $d) {
            $day = (int)($d['day'] ?? 0);
            $theme = htmlspecialchars($d['theme'] ?? '');
            $why = htmlspecialchars($d['why'] ?? '');
            $html .= '<div class="text-xs text-indigo-900">';
            $html .= '<span class="inline-block w-12 font-bold">Day ' . $day . '</span>';
            $html .= '<span class="font-semibold">' . $theme . '</span>';
            if ($why) $html .= ' — <span class="text-indigo-700">' . $why . '</span>';
            $html .= '</div>';
        }
        $html .= '</div>';
        $html .= '</div>';
    }

    $html .= '</div>';
    return $html;
}
