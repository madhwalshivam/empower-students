CREATE INDEX IF NOT EXISTS idx_wallet_ledger_parent_id ON public.wallet_ledger(parent_id);
ALTER TABLE public.wallet_ledger DISABLE ROW LEVEL SECURITY;

-- 9. CREATE TABLE: referrals
CREATE TABLE IF NOT EXISTS public.referrals (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    referrer_parent_id   UUID NOT NULL REFERENCES public.parents(id) ON DELETE CASCADE,
    referred_phone       TEXT,
    referred_email       TEXT,
    child_eval_completed BOOLEAN DEFAULT false,
    created_at           TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.referrals DISABLE ROW LEVEL SECURITY;

-- 10. CREATE TABLE: partner_payout_batches
CREATE TABLE IF NOT EXISTS public.partner_payout_batches (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    partner_id      UUID NOT NULL REFERENCES public.partners(id) ON DELETE CASCADE,
    period_start    TIMESTAMP WITH TIME ZONE,
    period_end      TIMESTAMP WITH TIME ZONE,
    total_credits   DOUBLE PRECISION NOT NULL,
    total_inr       DOUBLE PRECISION NOT NULL,
    item_count      INTEGER NOT NULL,
    method          TEXT,                         -- upi | bank_transfer | cash | other
    reference       TEXT,                         -- UTR / txn ID
    notes           TEXT,
    paid_by         TEXT,                         -- admin email/user
    paid_at         TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.partner_payout_batches DISABLE ROW LEVEL SECURITY;

-- 11. CREATE TABLE: partner_payouts
CREATE TABLE IF NOT EXISTS public.partner_payouts (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    partner_id          UUID NOT NULL REFERENCES public.partners(id) ON DELETE CASCADE,
    parent_id           UUID NOT NULL REFERENCES public.parents(id) ON DELETE CASCADE,
    wallet_ledger_id    BIGINT REFERENCES public.wallet_ledger(id) ON DELETE SET NULL,
    service_key         TEXT NOT NULL,
    gross_amount        INTEGER NOT NULL,
    partner_amount      DOUBLE PRECISION NOT NULL,
    share_rate_used     DOUBLE PRECISION NOT NULL,
    status              TEXT DEFAULT 'pending',   -- pending | paid | reversed
    batch_id            BIGINT REFERENCES public.partner_payout_batches(id) ON DELETE SET NULL,
    created_at          TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

CREATE INDEX IF NOT EXISTS idx_partner_payouts_partner_status ON public.partner_payouts(partner_id, status);
ALTER TABLE public.partner_payouts DISABLE ROW LEVEL SECURITY;

-- 12. CREATE TABLE: expert_report_orders
