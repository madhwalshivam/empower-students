-- Supabase Schema Initialization for Empower Students
-- Paste this script directly into your Supabase SQL Editor and click 'Run'.

-- 1. Enable any required extensions
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- 2. CREATE TABLE: partners (Linked to Supabase Auth)
CREATE TABLE IF NOT EXISTS public.partners (
    id              UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    name            TEXT NOT NULL,
    contact_name    TEXT,
    phone           TEXT,
    whatsapp        TEXT,
    email           TEXT,
    city            TEXT,
    referral_code   TEXT UNIQUE NOT NULL,         -- e.g. 'SUNRISE'
    revenue_share   DOUBLE PRECISION DEFAULT 0.30, -- 30% share
    bank_name       TEXT,
    bank_account    TEXT,
    bank_ifsc       TEXT,
    upi_id          TEXT,
    status          TEXT DEFAULT 'active',        -- active | paused | terminated
    notes           TEXT,
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    last_referral_at TIMESTAMP WITH TIME ZONE
);

-- Disable Row Level Security (RLS) to ensure Next.js Actions can interact freely with it
ALTER TABLE public.partners DISABLE ROW LEVEL SECURITY;

-- 3. CREATE TABLE: parents (Linked to Supabase Auth, optionally attributed to a partner)
CREATE TABLE IF NOT EXISTS public.parents (
    id            UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    partner_id    UUID REFERENCES public.partners(id) ON DELETE SET NULL,
    whatsapp      TEXT,
    name          TEXT,
    email         TEXT,
    city          TEXT,
    credits       INTEGER DEFAULT 0,
    is_vip        INTEGER DEFAULT 0,
    is_blocked    INTEGER DEFAULT 0,
    referral_code TEXT,
    created_at    TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    last_login    TIMESTAMP WITH TIME ZONE
);

-- Disable Row Level Security (RLS)
ALTER TABLE public.parents DISABLE ROW LEVEL SECURITY;
CREATE INDEX IF NOT EXISTS idx_parents_partner ON public.parents(partner_id);

-- 4. CREATE TABLE: children
CREATE TABLE IF NOT EXISTS public.children (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id     UUID NOT NULL REFERENCES public.parents(id) ON DELETE CASCADE,
    name          TEXT NOT NULL,
    gender        TEXT,
    dob           DATE NOT NULL,
    school        TEXT,
    class_grade   TEXT,
    mother_tongue TEXT,
    languages     TEXT,
    diagnosis     TEXT,
    notes         TEXT,
    photo         TEXT,
    created_at    TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.children DISABLE ROW LEVEL SECURITY;

-- 5. CREATE TABLE: assessments
CREATE TABLE IF NOT EXISTS public.assessments (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    child_id      BIGINT NOT NULL REFERENCES public.children(id) ON DELETE CASCADE,
    module        TEXT NOT NULL,
    age_band      TEXT,
    status        TEXT DEFAULT 'in_progress',
    score         DOUBLE PRECISION,
    level_reached TEXT,
    ai_summary    TEXT,
    flags         TEXT, -- JSON string
    raw_json      TEXT, -- JSON string
    created_at    TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    completed_at  TIMESTAMP WITH TIME ZONE
);

CREATE INDEX IF NOT EXISTS idx_assessments_child_id ON public.assessments(child_id);
CREATE INDEX IF NOT EXISTS idx_assessments_module ON public.assessments(module);
ALTER TABLE public.assessments DISABLE ROW LEVEL SECURITY;

-- 6. CREATE TABLE: child_eval_sessions
CREATE TABLE IF NOT EXISTS public.child_eval_sessions (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    child_id         BIGINT NOT NULL REFERENCES public.children(id) ON DELETE CASCADE,
    module           TEXT NOT NULL,
    age_at_session   DOUBLE PRECISION,
    target_turns     INTEGER,
    turn_count       INTEGER DEFAULT 0,
    status           TEXT DEFAULT 'in_progress',
    started_at       TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    last_activity_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    overall_score    DOUBLE PRECISION,
    report_json      TEXT, -- JSON string
    completed_at     TIMESTAMP WITH TIME ZONE
);

ALTER TABLE public.child_eval_sessions DISABLE ROW LEVEL SECURITY;

-- 7. CREATE TABLE: child_eval_turns
CREATE TABLE IF NOT EXISTS public.child_eval_turns (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id       BIGINT NOT NULL REFERENCES public.child_eval_sessions(id) ON DELETE CASCADE,
    turn_no          INTEGER NOT NULL,
    axis             TEXT,
    difficulty       INTEGER,
    question_json    TEXT,
    answer_json      TEXT,
    is_correct       BOOLEAN,
    score            DOUBLE PRECISION,
    feedback         TEXT,
    ai_meta_json     TEXT,
    response_seconds DOUBLE PRECISION,
    asked_at         TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    answered_at      TIMESTAMP WITH TIME ZONE
);

ALTER TABLE public.child_eval_turns DISABLE ROW LEVEL SECURITY;

-- 8. CREATE TABLE: wallet_ledger
CREATE TABLE IF NOT EXISTS public.wallet_ledger (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id     UUID NOT NULL REFERENCES public.parents(id) ON DELETE CASCADE,
    amount        INTEGER NOT NULL,
    balance_after INTEGER,
    service_key   TEXT,
    ref_id        BIGINT,
    reason        TEXT DEFAULT '',
    created_by    TEXT DEFAULT 'system',
